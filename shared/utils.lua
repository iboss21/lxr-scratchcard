function Utils.GetLocale(key, ...)
    local lang = Config.Lang or 'en'
    local locale = Config.Locale[lang]
    
    if not locale or not locale[key] then
        -- Fallback to English
        locale = Config.Locale['en']
    end
    
    if locale and locale[key] then
        return string.format(locale[key], ...)
    end
    
    return key
end

