function Utils.Trim(s)
    return s:match'^%s*(.*%S)' or ''
end

function Utils.Split(str, delimiter)
    local result = {}
    for match in (str..delimiter):gmatch("(.-)"..delimiter) do
        table.insert(result, match)
    end
    return result
end

